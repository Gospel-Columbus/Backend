
#Using While loop

i = 1

while i < 7:
    print(i)
    i += 1



#Using Break Statement
i = 1

while i < 8:
    print(i)
    if i == 3:
        break
    i += 1




#Using Continue Statement
i = 0

while i < 8:
    i += 1
    if i == 4:
        continue
    print(i)
